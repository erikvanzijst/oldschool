<p>
<b>David van Moolenbroek: Attack of the Lizardmen (grade: 10)</b></p>
<p>
An advanced first person shooter, including shooting of textured particles and interaction with the dynamic enviroment. 
A level editor is also available. The new all-time high-scoring project.

