<h2>2002</h2>

<p>
<b>T. Tusarova: Dino project (grade: 10)</b></p>
<p>
An army of jumping and marching dinosaurs and one general. The general can be controlled
by the user or set to a random walk over the globe. The camera can be moved around and away from
the globe, which is covered by randomly placed 3D-trees. Collision detection is used to
prevent the general from walking through trees or other dino's.</p>
