<H2>OpenGL references</H2>

<UL>
<LI><B><A HREF="http://www.3dlabs.com/support/developer/glmanpage_index.htm">OpenGL 1.1 function reference</A></B><BR>
    Online HTML documentation of the <CODE>gl*</CODE> functions.</LI>
<LI><B><A HREF="http://www.cevis.uni-bremen.de/~uwe/opengl/opengl.html">OpenGL 1.0 function reference (old standard)</A></B><BR>
    Online HTML documentation of the <CODE>gl*</CODE>, <CODE>glx*</CODE> and <CODE>glu*</CODE> functions. There's also an <A HREF="http://www.cevis.uni-bremen.de/~uwe/opengl/" TARGET="_blank">offline version</A> available.</LI>
<LI><B><A HREF="http://www.cs.vu.nl/~graphics/glut_html/index.html">GLUT API version 3</A></B><BR>
    Local copy of the GLUT API reference; lookup <CODE>glut*</CODE> functions here.</LI>
<LI><B><A HREF="http://fly.cc.fer.hr/~unreal/theredbook">OpenGL Programming Guide (Red Book)</A></B><BR>
    An alternative to the book, mainly focused on programming.</LI>
<LI><B><A HREF="Http://www.sgi.com/software/opengl/advanced98/notes/notes.html">Advanced Graphics Programming Techniques Using OpenGL</A></B><BR>
    SIGGRAPH `98 course covering a lot of advanced techniques.</LI>
<LI><B><A HREF="http://www.google.com">Google</A></B><BR>
    To find everything you haven't found yet :).</LI>
</UL>

