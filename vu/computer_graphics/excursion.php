<H2>Excursion to the CAVE at SARA (Sep 22, 2004)</H2>

<P>
September 22nd, we will demonstrate the capabilities of a "high-end" 
graphics system. This is the 
<A HREF="http://www.sara.nl/products/products_08a_01_eng.html">
CAVE</a>, located at SARA (A'dam Watergraafsmeer).

We will meet at the front door of the SARA building. You will have to get
there by yourself.

</P>

How to get there:
<ul>
<li> This is the address of SARA:
<B><P>SARA Computing and Networking Services<BR>
Kruislaan 415<BR>
1098 SJ Amsterdam</P></B>

<li> For those that wish to travel by public transportation, look at the <a href="http://www.wtcw.nl/en/contact/route.html">bus schedule</a> for bus 67 from Amsterdam Amstel Station to the WTCW campus (where SARA is located). 
<li> Finally, a small
<a href="images/saramap.png">map</a> of the WTCW campus shows you the building.
(Please note that we meet at SARA, not NIKHEF. Unfortunately, SARA did
 not have a map online, so I had to improvise...)
 </ul>


